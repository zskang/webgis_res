package com.cabletech.res.entity.analyse;

import java.io.Serializable;

/**
 * 统计分析实体类
 * 
 * @author 吕仁钊 2012-08-11
 * 
 */
public class AnalyseEntity implements Serializable {
	private static final long serialVersionUID = 1L;

	private String id;
	private String resource_type;
	private String statistics_type;
	private Object palte_name;
	private Object statistics_sql;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getResource_type() {
		return resource_type;
	}

	public void setResource_type(String resourceType) {
		resource_type = resourceType;
	}

	public String getStatistics_type() {
		return statistics_type;
	}

	public void setStatistics_type(String statisticsType) {
		statistics_type = statisticsType;
	}

	public Object getPalte_name() {
		return palte_name;
	}

	public void setPalte_name(Object palteName) {
		palte_name = palteName;
	}

	public Object getStatistics_sql() {
		return statistics_sql;
	}

	public void setStatistics_sql(Object statisticsSql) {
		statistics_sql = statisticsSql;
	}

}
