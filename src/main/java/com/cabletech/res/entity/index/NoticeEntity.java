package com.cabletech.res.entity.index;

import java.io.Serializable;

import com.cabletech.baseinfo.business.rest.BaseEntity;

/**
 * 统计分析实体类
 * 
 * @author 吕仁钊 2012-08-11
 * 
 */
public class NoticeEntity extends BaseEntity implements Serializable {
	private static final long serialVersionUID = 1L;

	private String id;
	private String title;
	private String type;
	private String content;
	private String regionid;
	private String issueperson;

	public String getIssueperson() {
		return issueperson;
	}

	public void setIssueperson(String issueperson) {
		this.issueperson = issueperson;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getRegionid() {
		return regionid;
	}

	public void setRegionid(String regionid) {
		this.regionid = regionid;
	}

}
